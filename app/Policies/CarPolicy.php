<?php

namespace App\Policies;

use App\Models\Car;
use App\Models\User;

/**
 * Политика авторизации для автомобилей
 *
 * Определяет правила доступа пользователей к операциям с автомобилями.
 * Каждый автомобиль принадлежит только одному пользователю и может быть
 * доступен только своему владельцу.
 */
class CarPolicy
{
    /**
     * Проверка права на просмотр автомобиля
     *
     * Пользователь может просматривать только свои автомобили.
     *
     * @param User $user Текущий аутентифицированный пользователь
     * @param Car $car Автомобиль, к которому запрашивается доступ
     * @return bool true если пользователь является владельцем автомобиля, иначе false
     *
     */
    public function view(User $user, Car $car): bool
    {
        return $user->id === $car->user_id;
    }

    /**
     * Проверка права на обновление автомобиля
     *
     * Пользователь может обновлять только свои автомобили.
     *
     * @param User $user Текущий аутентифицированный пользователь
     * @param Car $car Автомобиль, который пытаются обновить
     * @return bool true если пользователь является владельцем автомобиля, иначе false
     *
     */
    public function update(User $user, Car $car): bool
    {
        return $user->id === $car->user_id;
    }

    /**
     * Проверка права на удаление автомобиля
     *
     * Пользователь может удалять только свои автомобили.
     *
     * @param User $user Текущий аутентифицированный пользователь
     * @param Car $car Автомобиль, который пытаются удалить
     * @return bool true если пользователь является владельцем автомобиля, иначе false
     *
     */
    public function delete(User $user, Car $car): bool
    {
        return $user->id === $car->user_id;
    }

    /**
     * Проверка права на создание автомобиля
     *
     * Любой аутентифицированный пользователь может создавать автомобили.
     * Автомобиль автоматически привязывается к текущему пользователю.
     *
     * @param User $user Текущий аутентифицированный пользователь
     * @return bool Всегда true для аутентифицированных пользователей
     *
     */
    public function create(User $user): bool
    {
        return true;
    }
}
